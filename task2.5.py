# 5. Список професій
professions = ["Doctor", "Engineer", "Teacher", "Artist", "Scientist"]

# Декілька прикладів роботи зі списками
print("Original list:", professions)
print("Sorted list:", sorted(professions))
professions.append("Pilot")
print("After appending 'Pilot':", professions)
professions.remove("Doctor")
print("After removing 'Doctor':", professions)
print("Length of list:", len(professions))
print("First profession:", professions[0])
print("Last profession:", professions[-1])
